    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        DAO.ins.loadProduct();
        DAO.ins.loadProducer();
        request.setAttribute("mng", DAO.ins);
        int nrpp = 6, index = -1; //update later
        try {
            nrpp = Integer.parseInt(request.getParameter("nrpp"));
        } catch (Exception e) {
        }
        try {
            index = Integer.parseInt(request.getParameter("index"));
        } catch (Exception e) {
        }
        Paging p = new Paging(nrpp, index, DAO.ins.getProduct().size());
        p.calc();
        request.setAttribute("p", p);
        request.setAttribute("cate", DAO.ins.getProducer());
        request.getRequestDispatcher("manage.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String name = request.getParameter("name");
        String img = request.getParameter("image");
        int price = Integer.parseInt(request.getParameter("price"));
        int quantity = Integer.parseInt(request.getParameter("quantity"));
        int producerID = Integer.parseInt(request.getParameter("producer"));
        DAO.ins.addProduct(price,name,quantity,img,producerID);
        doGet(request, response);
    }

