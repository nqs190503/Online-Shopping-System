    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession ses = request.getSession();
        User a = (User) ses.getAttribute("user");

        User user = DAO.ins.getUserByUserID(a.getUserID());

        DAO.ins.loadOrderWithUserID(user.getUserID());
        DAO.ins.loadOrderDetailWithUserID(user.getUserID());
        HashMap u = DAO.ins.loadUserWithHashMap();

        HashMap product = DAO.ins.loadProductWithHashMap();

        ses.setAttribute("ses", user);
        request.setAttribute("order", DAO.ins.getOrder());
        request.setAttribute("orderdetail", DAO.ins.getOrderdetail());
        request.setAttribute("userlist", u);
        request.setAttribute("productlist", product);
        request.getRequestDispatcher("myaccount.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        if (request.getParameter("update") != null) {
            int userID = Integer.parseInt(request.getParameter("uuserID"));
            String uName = request.getParameter("uName");
            int uContact = Integer.parseInt(request.getParameter("uContact"));
            String uAddress = request.getParameter("uAddress");
            DAO.ins.updateProfile(userID, uName, uContact, uAddress);
            doGet(request, response);
        }

    }

